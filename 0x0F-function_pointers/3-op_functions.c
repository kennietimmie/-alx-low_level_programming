/**
 * op_add - add two integers
 * @a: int a
 * @b: int b
 *
 * Return: Always int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: int a
 * @b: int b
 *
 * Return: Always int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product of two integers
 * @a: int a
 * @b: int b
 *
 * Return: Always int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two integers
 * @a: int a
 * @b: int b
 *
 * Return: Always int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division
 * @a: int a
 * @b: int b
 *
 * Return: Always int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
